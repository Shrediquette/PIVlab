            'Label',getWavMSG('Wavelet:dw1dRF:Lab_AppCfs'), ...
            'Position',5,             ...
            'Separator','On',...
            'Tag','Men_Save_APP_CFS' ...
            );
        uimenu(Men_Save_APP_CFS,...
            'Label',getWavMSG('Wavelet:dw1dRF:Lab_AppCfsOf',1), ...
            'Position',1,             ...
