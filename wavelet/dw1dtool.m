            'Position',2,             ...
            'Callback',@(~,~)dw1dmngr('load_cfs', win_dw1dtool )  ...
            );
        uimenu(m_load,...
            'Label',getWavMSG('Wavelet:dw1dRF:Lab_Decomposition'), ...
            'Position',3,             ...
            'Callback',@(~,~)dw1dmngr('load_dec', win_dw1dtool )  ...
            );
		uimenu(m_save,...
            'Label',getWavMSG('Wavelet:dw1dRF:Lab_Signal'),...
            'Position',1,                 ...
            'Callback',@(~,~)dw1dmngr('save_synt', win_dw1dtool )  ...
            );
        uimenu(m_save,...
            'Label',getWavMSG('Wavelet:dw1dRF:Lab_Coefficients'), ...
            'Position',2,            ...
            'Callback',@(~,~)dw1dmngr('save_cfs', win_dw1dtool )  ...
            );
