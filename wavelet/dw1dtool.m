            'Position',5,'Enable','Off','Separator','Off', ...
            'Tag',tag_m_exp_wrks  ...
            );
                
        uimenu(m_load,...
            'Label',getWavMSG('Wavelet:dw1dRF:Lab_Signal'),   ...
            'Position',1,'Tag','Load_Sig',        ...
            'Callback',@(~,~)dw1dmngr('load_sig', win_dw1dtool ) ...
