            'Tag','Basic_Sig');
        m_demo_2 = uimenu(m_demo,'Label',getWavMSG('Wavelet:dw1dRF:Lab_Demo_2'));
        m_demo_3 = uimenu(m_demo,'Label',getWavMSG('Wavelet:dw1dRF:Lab_Demo_3'));

