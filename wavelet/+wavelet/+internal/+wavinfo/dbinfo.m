%   Biorthogonal            yes
%   Compact support         yes
%   DWT                     possible
%   CWT                     possible
%
%   Support width           2N-1
%   Filters length          2N
%   Regularity              about 0.2 N for large N
%   Symmetry                far from
%   Number of vanishing 
%   moments for psi         N
%
